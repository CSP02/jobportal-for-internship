        <h1>How to Register:</h1>
        <p>For register into the website click on the Register button on the top-right corner of this page. Now fill the required details like username, password and user type. User type is of two types admin and applicant. Admins are the ones who post their job(in simple words advertise their jobs) where applciants can apply for those jobs.</p>
        <h1>How to access dashboard:</h1>
        <p>Accessing dashboard is very simple in this portal after signing in just click on your username to navigate to your dashboard. Both admin and applicant has their own dashboards where user can view their details of the application.</p>
        <h1>How to apply for a job:</h1>
        <p>At the home page you can view the job listings in by clicking explore button or scrolling down. Where you want to apply for a job you can just click on the advertisement and that's it. Your details will be sent to the author of the advertisement.</p>
        <h1>How to post an advertisement:</h1>
        <p>If you are an admin you can advertise your jobs. After signing in to your account, click on your username on the top right corner of the home page and you will be redirected to your dashboard. Now click on the post job button and fill the required details. After that click on post.</p>
        <h1>How to get the applicants list(in admin dashboard):</h1>
        <p>Click on the ">" button at the left side(for mobile users) and it will open the user menu for desktop sites the user menu is visible on the left side of the page. Here you can view different categories like your jobs, applicants, etc. click on the applicants and you can view details of applicants and for what job they applied for.</p>
        <h1>Posted jobs are not showing in the dashboard:</h1>
        <p>If your postings/applicants/applied jobs are not showing in the dashboard you can refresh the dashboard by clicking on the refresh button on the top right corner of the table and beside post job button.</p>
